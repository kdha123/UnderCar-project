package com.undercar.event.dto;

public class EventDTO {
	
	private int no;
	private String title;
	private String image;
	private String startDate;
	private String endDate;
	private String writeDate;
	private String winnerDate;
	private String repImage;
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	public String getEndDate() {
		return endDate;
	}
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	public String getWriteDate() {
		return writeDate;
	}
	public void setWriteDate(String writeDate) {
		this.writeDate = writeDate;
	}
	public String getWinnerDate() {
		return winnerDate;
	}
	public void setWinnerDate(String winnerDate) {
		this.winnerDate = winnerDate;
	}
	public String getRepImage() {
		return repImage;
	}
	public void setRepImage(String repImage) {
		this.repImage = repImage;
	}
	@Override
	public String toString() {
		return "EventDTO [no=" + no + ", title=" + title + ", image=" + image + ", startDate=" + startDate
				+ ", endDate=" + endDate + ", writeDate=" + writeDate + ", winnerDate=" + winnerDate + ", repImage="
				+ repImage + "]";
	}
	
}
