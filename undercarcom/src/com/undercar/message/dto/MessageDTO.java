package com.undercar.message.dto;

public class MessageDTO {
	
	private int no;
	private String title;
	private String content;
	private String sender;
	private String sendDate;
	private String accepter;
	private String acceptDate;
	private int sendel;
	private int accdel;
	private int allCheck;
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getSender() {
		return sender;
	}
	public void setSender(String sender) {
		this.sender = sender;
	}
	public String getSendDate() {
		return sendDate;
	}
	public void setSendDate(String sendDate) {
		this.sendDate = sendDate;
	}
	public String getAccepter() {
		return accepter;
	}
	public void setAccepter(String accepter) {
		this.accepter = accepter;
	}
	public String getAcceptDate() {
		return acceptDate;
	}
	public void setAcceptDate(String acceptDate) {
		this.acceptDate = acceptDate;
	}
	public int getSendel() {
		return sendel;
	}
	public void setSendel(int sendel) {
		this.sendel = sendel;
	}
	public int getAccdel() {
		return accdel;
	}
	public void setAccdel(int accdel) {
		this.accdel = accdel;
	}
	public int getAllCheck() {
		return allCheck;
	}
	public void setAllCheck(int allCheck) {
		this.allCheck = allCheck;
	}
	
	@Override
	public String toString() {
		return "MessageDTO [no=" + no + ", title=" + title + ", content=" + content + ", sender=" + sender
				+ ", sendDate=" + sendDate + ", accepter=" + accepter + ", acceptDate=" + acceptDate + ", sendel="
				+ sendel + ", accdel=" + accdel + ", allCheck=" + allCheck + "]";
	}

}// end of MessageDTO
